// Helper function to get timezone options for a country
export function getTimezoneOptions(countryCode: string): { label: string; value: string }[] {
    const timezones = timezonesByCountry[countryCode] || [];
    return timezones.map(tz => ({
        label: tz.replace(/_/g, " "),
        value: tz
    }));
}

// Helper function to get currency for a country (returns code like "USD")
export function getCurrencyForCountry(countryCode: string): string {
    return currencyByCountry[countryCode]?.code || "";
}

// Helper function to get currency display string (e.g. "USD - US Dollar")
export function getCurrencyDisplayForCountry(countryCode: string): string {
    const currency = currencyByCountry[countryCode];
    if (!currency) return "";
    return `${currency.code} - ${currency.name}`;
}
